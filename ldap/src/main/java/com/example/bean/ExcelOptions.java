package com.example.bean;

public class ExcelOptions {
	
	String Yes;
	String No;
	
	public ExcelOptions(String yes, String no) {
		super();
		Yes = yes;
		No = no;
	}

	public String getYes() {
		return Yes;
	}

	public void setYes(String yes) {
		Yes = yes;
	}

	public String getNo() {
		return No;
	}

	public void setNo(String no) {
		No = no;
	}
	

}
